/* Em Javascript não precisa se declara o tipo da variavel, é tipado automaticamente 
number: para declara um numero inteiro ou decimal
string: para uma string de caracteres
boolean: para declarar um valor boleano
object: para declarar um objeto
array: para declarar um array
*/

/* 1 - Declare uma variável do tipo inteiro com nome valor 1 */

let /*number*/ valor_1 = 3;
console.log(valor_1)

/* -------------------------------------------------------------------------- */
/* 2 - Declare uma variavel "cadeia" com o nome frase*/

let /*array*/ frase = "Ola";
console.log(frase)

/* -------------------------------------------------------------------------- */
/* 3 - Declare uma constante do tipo "inteiro" que tenha o nome de nome*/

const /*number*/ nome = 2
