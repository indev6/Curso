
// Escreva um algoritimo que calcule a soma do numeros de 1 a 15

public class Atividade2 {

    public static void main(String[] args) {
        
        int n1, soma = 0;

        for(n1 = 1; n1 <= 15; n1++) {

            soma = soma + n1;

        }

        
        System.out.println(soma);

    }

}
